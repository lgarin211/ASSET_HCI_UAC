let clap = 0;
let waz = 2;

function Fexli(caww) {
    document.getElementById("Charte").innerHTML = ""
    for (var i = clap; i <= waz; i++) {
        document.getElementById("Charte").innerHTML +=
            `
        <div class="mostpoinscard " onclick="ozibopot(${i},${caww})">
            <div class="CRON">
                <div class="COSTPON flowup2">
                    <img src="${mapsall[caww].char[i].img}" alt="" class="flowup2">
                </div>
                <div class="TEXTPOS flowup2">
                    <h1>${mapsall[caww].char[i].name}</h1>
                </div>
            </div>
        </div>
        `
    }

    ozibopot(0, caww)


    var aw = document.getElementById("wppr").style =
        `background: url('${mapsall[caww].img}');background-size: cover;background-position: center;background-repeat: no-repeat;background-attachment: fixed;`
    console.log(aw)
    document.getElementById("MapsActive").innerHTML = mapsall[caww].name
}

function optMaps() {
    document.getElementById("MapsList").innerHTML = ""
    for (var i = 0; i < Object.keys(mapsall).length; i++) {
        document.getElementById("MapsList").innerHTML +=
            `<div class="conve" onclick="Fexli(${i})">
            <h1 onclick="buzz(this)" class="flox">${mapsall[i].name}</h1>
        </div>`
    }
}

function buzz(elmp) {
    var a = document.getElementsByClassName("flox")
    for (let i = 0; i < a.length; i++) {
        a[i].style = ""
    }
    console.log(elmp)
    elmp.style = `filter: drop-shadow(0px 5px 10px #FFFFFF);transform: scale(1.1);transition: 0.5s;`
}

var blen=0
var vblun=0
function ozibopot(ipo, caww) {
    document.getElementById("Cjp").src = mapsall[caww].char[ipo].imgBig
    document.getElementById("NameCh").innerHTML = mapsall[caww].char[ipo].name
    document.getElementById("NameWep").innerHTML = mapsall[caww].char[ipo].weapon
    document.getElementById("IMGWepC").src = mapsall[caww].char[ipo].elementimg
    document.getElementById("FOS").innerHTML = mapsall[caww].char[ipo].element
    document.getElementById("BignoidIMG").src = mapsall[caww].char[ipo].BigNoid
    document.getElementById("Dtext-link").innerHTML = mapsall[caww].char[ipo].desc
    blen=caww
    vblun=ipo
}


function fasup() {
    vblun++
    if(vblun>3){
        vblun=0
    }
    ozibopot(vblun,blen)
    
}


function fasdown() {
    vblun--
    if(vblun<0){
        vblun=3
    }
    ozibopot(vblun,blen)
}

